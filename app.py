            for i in range(60, len(scaled) - steps):
                X.append(scaled[i-60:i].flatten())
                y.append(scaled[i+steps-1][0])
            if not X:
                return np.nan
            X, y = np.array(X), np.array(y)

            if model_type == "Linear":
                model = LinearRegression()
